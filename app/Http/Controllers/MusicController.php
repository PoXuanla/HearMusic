    {
        //
        $TopTenMusic = Music::all();
        $musics = array();
        foreach ($TopTenMusic as $mu){
            $object = new \stdClass();
            $object->name = $mu->name;
            $object->user_name = User::select('name')->where('id',$mu->user_id)->get()[0]->name;
            $object->image = $mu->image;
            array_push($musics,$object);
        }
        return view('home/index',compact('musics'));
    }

